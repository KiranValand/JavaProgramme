package PracticeLearning;

// Java Program to Check if a Given Integer is Positive or Negative

public class posNeg {

    public static void main(String[] args)
    {
        int Number =0;
        if (Number > 0)
            System.out.print(Number + "  is Positive number ");
        else if(Number < 0)
            System.out.print(Number + "  is Negative ");
        else if (Number == 0)
            System.out.print(Number + "   zero is Neutral. ");
    }
}